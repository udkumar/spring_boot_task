package com.ikea.filehandling;

public class FileUtilLongest {
    String first;
    String second;

    public FileUtilLongest(String first, String second) {
        this.first = first;
        this.second = second;
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
